/**
 * @brief 타이머 주기 완료 콜백 (10ms마다 호출)
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM2)
    {
        // ========== 수동 모드 타이머 감소 ==========
        if (control_mode == 1)
        {
            if (manual_pan_timer > 0)
            {
                manual_pan_timer--;
            }

            if (manual_tilt_timer > 0)
            {
                manual_tilt_timer--;
            }
        }

        // ========== 하트비트 송신 (500ms) ==========
        if (++heartbeat_counter >= HEARTBEAT_PERIOD)
        {
            heartbeat_counter = 0;
            CAN_SendGimbalHeartbeat();
        }
    }
}

